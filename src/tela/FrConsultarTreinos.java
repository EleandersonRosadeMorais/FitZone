    private void btnDeletarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeletarMouseClicked
        ControladorDeTreino conTre = new ControladorDeTreino();
        Instrutor instrutorLogado = InstrutorLogado.getInstrutorLogado();
        if (conTre.verificar(instrutorLogado.getPkInstrutor())) {

            if (tblTreinos.getSelectedRow() == -1) {
                return;
            }
            int posicaoLinha = tblTreinos.getSelectedRow();

            String celula = tblTreinos.getValueAt(posicaoLinha, 0).toString();

            if (conTre.deletar(celula)) {
                JOptionPane.showMessageDialog(null, "Treino Deletado");
                pesquisar();
            } else {
                JOptionPane.showMessageDialog(null, "Falha ao deletar");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Voce naum pode excluir algum treino que nao foi voce quem criou");
        }
    }//GEN-LAST:event_btnDeletarMouseClicked

    private void btnPesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPesquisarMouseClicked
        pesquisar();
    }//GEN-LAST:event_btnPesquisarMouseClicked
    private void pesquisar() {
        DefaultTableModel modeloTabela = (DefaultTableModel) tblTreinos.getModel();

        modeloTabela.setNumRows(0);

        int opcaoFiltro = cbxFiltro.getSelectedIndex();

        ControladorDeTreino conTre = new ControladorDeTreino();

        Instrutor instrutorLogado = InstrutorLogado.getInstrutorLogado();

        List<Treino> listaTreinos = conTre.consultarOpcao(opcaoFiltro, instrutorLogado.getPkInstrutor());

        for (Treino tre : listaTreinos) {
            Object[] linha = {
                tre.getNome(),
                tre.getDescricao(),
                tre.getObjetivo(),
                tre.getDuracao_minutos(),
                tre.isConcluido()
            };

            modeloTabela.addRow(linha);
        }
        lblTotalRegistros.setText("Total registros: " + listaTreinos.size());
    }

